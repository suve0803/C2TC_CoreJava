package com.cg.dayone;

public class Basics {

	public static void main(String[] args) {
		int a=10,b=30;
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b));
		System.out.println("a%b="+(a%b));
		System.out.println("a>b="+(a>b));
		System.out.println("a<b="+(a<b));
		System.out.println("a==b="+(a==b));
		System.out.println("Increment="+(a++));
		System.out.println("Decrement="+(a--));

	}

}
